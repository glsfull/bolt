  id: string;
  date: string;
  type: string;
  typeLabel: string;
  fileName: string;
  markersCount: number;
  abnormalCount: number;
  healthScore: number;
  markers: { name: string; value: number; unit: string; status: 'low' | 'normal' | 'high' }[];
